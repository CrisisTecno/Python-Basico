# Operaciones con Listas

# Crear una lista de números del 1 al 5
numeros = [1, 2, 3, 4, 5]

# Imprimir la lista original
print("Lista de números:", numeros)

# Sumar todos los números en la lista usando la función `sum()`
suma = sum(numeros)
print("Suma de los números:", suma)

# Encontrar el número máximo en la lista usando `max()`
maximo = max(numeros)
print("Número máximo:", maximo)

# Ordenar la lista de forma ascendente usando `sorted()`
lista_ordenada = sorted(numeros)
print("Lista ordenada:", lista_ordenada)